                pdob.setText(birth);
                ddob.setText(birth);
                String gender = rs.getString("gender");
                pgender.setText(gender);
                dgender.setText(gender);
                String stn = rs.getString("stain");
                calculus.setText(stn);
                String ht = rs.getString("height");
                height.setText(ht);
